#!/bin/bash

# 快速清理 node_modules 脚本
# Fast Clean Node Modules Script

echo "🧹 快速清理 node_modules..."

# 颜色定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# 检查是否在正确的目录
if [ ! -d "frontend" ]; then
    echo -e "${RED}❌ 错误：请在项目根目录运行此脚本${NC}"
    exit 1
fi

cd frontend

if [ -d "node_modules" ]; then
    echo -e "${YELLOW}正在清理 frontend/node_modules...${NC}"
    
    # 使用最快的删除方法
    if command -v rsync &> /dev/null; then
        echo -e "${BLUE}使用 rsync 快速清理...${NC}"
        rsync -a --delete /dev/null/ node_modules/
        rmdir node_modules
    elif command -v find &> /dev/null; then
        echo -e "${BLUE}使用 find 优化清理...${NC}"
        find node_modules -delete 2>/dev/null
        rmdir node_modules 2>/dev/null
    else
        echo -e "${BLUE}使用标准 rm 命令...${NC}"
        rm -rf node_modules
    fi
    
    # 清理 package-lock.json
    rm -f package-lock.json
    
    echo -e "${GREEN}✅ node_modules 清理完成${NC}"
else
    echo -e "${GREEN}✅ node_modules 不存在，无需清理${NC}"
fi

cd ..

echo -e "${GREEN}🎉 清理完成！现在可以运行 ./start.sh 重新安装依赖${NC}" 